class Dog {
    // Instance variables
    private String name;
    private String breed;
    private int age;

    // Constructor
    public Dog(String nm, String brd, int ag) {
        nm = name;
        ag = age;
        brd = breed;
    }

    // Getters
    public void bark() {
        System.out.println("Woof!");
    }
}

